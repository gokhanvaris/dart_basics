 Dart Kontrol Akış Bildirimi
kontrol ifadeleri veya kontrol ifadelerinin akışı Dart programının akışını kontrol etmek için kullanılır. 
Bu ifadeler, herhangi bir programlama dilinde, başka bir ifadenin yürütülüp yürütülmeyeceğine karar vermek için çok önemlidir. 
Kod ifadesi genellikle sıralı şekilde çalışır. 
Verilen koşula göre bir grup ifadenin yürütülmesini veya atlanmasını, başka bir ifadeye atlamayı veya ifadelerin yürütülmesini tekrarlamayı gerektirebiliriz.

Dart'ta kontrol ifadesi, programın düzgün akışına izin verir. 
Kontrol akış ifadeleri kullanılarak, bir Dart programı uygulama mantığına göre değiştirilebilir, yeniden yönlendirilebilir veya tekrarlanabilir.

Akış Bildirimi Kategorileri

Dart'ta, Kontrol akış bildirimi temel olarak aşağıdaki üç şekilde kategorize edilebilir.

Karar verme beyanları
Döngü ifadeleri
Atlama ifadeleri
Dart Karar Verme Beyanları

Karar verme ifadeleri, çalışma zamanında test ifadesine dayanarak hangi ifadenin yürütüleceğini belirlememize olanak tanır. 
Karar verme beyanları Seçim beyanları olarak da bilinir. 
Dart programında, Boole TRUE ve FALSE'ı değerlendiren tek veya çoklu test ifadesi ( veya koşul ) mevcut olabilir. 
İfade / koşulun bu sonuçları, verilen koşul DOĞRU veya YANLIŞ ise hangi ( s ) ifade bloğunun uygulanacağına karar vermeye yardımcı olur.
Dart aşağıdaki Karar verme beyanlarını sunmaktadır.

If Statement
If-else Statements
If else if Statement
Switch Case Statement

Dart Döngü Beyanları
Dart döngü ifadeleri, verilen koşulla eşleşene kadar belirli bir süre için kod bloğunu birden çok kez yürütmek için kullanılır. Bu ifadelere Yineleme ifadesi de denir.

Dart aşağıdaki döngü ifadelerini sağlar.

Dart for loop
Dart for….in loop
Dart while loop
Dart do while loop

Dart Atlama Beyanları
Atlama ifadeleri başka bir ifadeden atlamak için kullanılır veya yürütmeyi geçerli ifadeden başka bir ifadeye aktardığını söyleyebiliriz.

Dart aşağıdaki atlama ifadelerini sağlar -

Dart Break Statement
Dart Continue Statement
